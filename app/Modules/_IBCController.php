    public function request($function, $params)
    {
        $time = time();
        $method = "POST";
        $logForDB = [
            'channel' => ModelsLog::CHANNEL_IBC,
            'function' => $function,
            'params' => json_encode($params),
            'method' => $method,
        ];




        $this->create_param($function, $params);

        try {
            $client = new \GuzzleHttp\Client();
            $response = $client->post($this->get_url($function), [
                'on_stats' => function (\GuzzleHttp\TransferStats $stats) use ($time) {
                },
                'form_params' => $this->make_params($function),
            ]);

            $response = @json_decode($response->getBody(), true);
            $logForDB['status'] = ModelsLog::STATUS_SUCCESS;
            $logForDB['trace'] = json_encode($response);
        } catch (Exception $e) {
            $logForDB['status'] = ModelsLog::STATUS_ERROR;
            $logForDB['trace'] = $e->getMessage();
            ModelsLog::addLog($logForDB);

            return [
                'status' => false,
                'status_code' => null,
                'status_message' => "Unknown ERROR",
                'data' => null,
            ];
        }

        if (!$response) {
            $logForDB['status'] = ModelsLog::STATUS_ERROR;
            $logForDB['trace'] = "$time Status: Unknown";
            ModelsLog::addLog($logForDB);

            return [
                'status' => false,
                'status_code' => null,
                'status_message' => "Unknown ERROR",
                'data' => null,
            ];
        }

        if (isset(SELF::ERRORS[$function][$response['error_code']])) {
            $message =  SELF::ERRORS[$function][$response['error_code']];
        } else {
            $message = "Unknown Error";
        }
        $logForDB['message'] = $message;

        if ($function == "CheckFundTransfer") {
            if ($response['error_code'] == 3 || $response['error_code'] == 2) {
                return [
                    'status' => true,
                    'status_code' => $response['error_code'],
                    'status_message' => $response['message'],
                    'data' => isset($response['Data']) ? $response['Data'] : null,
                ];
            }
        }

        if($response['error_code'] != 0){
            $logForDB['status'] = ModelsLog::STATUS_ERROR;
            ModelsLog::addLog($logForDB);
        }

        return [
            'status' => $response['error_code'] == 0 ? true : false,
            'status_code' => $response['error_code'],
            'status_message' => $response['message'],
            'data' => isset($response['Data']) ? $response['Data'] : null,
        ];
    }

